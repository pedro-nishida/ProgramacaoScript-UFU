#Entrada
a = int(input()) #primeiro número recebido
b = int(input()) #segundo número recebido

#Cálculos dos números recebidos:

soma = a + b #soma
subt = a - b #subtração
mult = a * b #multiplicação
div_inteiro = a // b #divisão inteira
div_resto = a % b #resto da divisão

#Saída
print("a =",a)
print("b =",b)
print("a + b =",soma)
print("a - b =",subt)
print("a * b =",mult)
print("a // b =",div_inteiro)
print("a % b =",div_resto)